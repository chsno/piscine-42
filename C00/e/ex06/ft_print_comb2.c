#include <unistd.h>

void	ft_putchar(char a)
{
	write(1, &a, 1);
}

void	multi_putchar(int a, int b)
{
	ft_putchar((a / 10) + '0');
	ft_putchar((a % 10) + '0');
	ft_putchar(' ');
	ft_putchar((b / 10) + '0');
	ft_putchar((b % 10) + '0');
	if (a != 98 || b != 99)
	{
		ft_putchar(',');
		ft_putchar(' ');
	}
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = 0;
	while (a < 99)
	{
		b = a +1;
		while (b < 100)
		{
			multi_putchar(a, b);
			b++;
		}
		a++;
	}
}
