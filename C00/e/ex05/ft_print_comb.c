#include <unistd.h>

void	ft_print_comb(void)
{
	char	q;
	char	w;
	char	e;

	q = 48;
	while (q < 58)
	{
		w = q + 1;
		while (w < 58)
		{
			e = w + 1;
			while (e < 58)
			{
				write(1, &q, 1);
				write(1, &w, 1);
				write(1, &e, 1);
				if (q != 55 || w != 56 || e != 57)
					write(1, ", ", 2);
				e++;
			}
		w++;
		}
	q++;
	}
}
