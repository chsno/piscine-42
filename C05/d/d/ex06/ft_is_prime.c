int	ft_is_prime(long nb)
{
	long	i;

	i = 2;
	if (nb % 2 == 0 && nb != 2)
		return (0);
	if (nb <= 1)
		return (0);
	while (i * i <= nb)
	{
		if ((nb % i) == 0)
			return (0);
		i++;
	}
	return (1);
}
